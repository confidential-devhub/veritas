"""Data models for CoCo reference values."""

import json
from dataclasses import dataclass


RVPS_EXPIRATION = "2099-12-31T00:00:00Z"
RVPS_NAMESPACE = "trustee-operator-system"


@dataclass
class ReferenceValue:
    """A single reference value for Trustee RVPS."""

    name: str
    values: list[str]
    category: str  # executables, hardware, configuration
    description: str
    algorithm: str  # sha256, sha384
    source: str


def format_trustee(values: list[ReferenceValue], platform: str, tee: str,
                   versions: list[str] | None = None) -> str:
    """Format values as a Trustee RVPS ConfigMap YAML."""
    rvps_entries = [
        {"name": v.name, "expiration": RVPS_EXPIRATION, "value": v.values}
        for v in values
    ]
    reference_values = json.dumps(rvps_entries, indent=2)

    header = f"# Generated by veritas (platform: {platform}, tee: {tee})\n"
    if versions:
        header += f"# Versions: {', '.join(versions)}\n"

    return (
        header +
        f"apiVersion: v1\n"
        f"kind: ConfigMap\n"
        f"metadata:\n"
        f"  name: rvps-reference-values\n"
        f"  namespace: {RVPS_NAMESPACE}\n"
        f"data:\n"
        f"  reference-values: |\n"
    ) + "\n".join(f"    {line}" for line in reference_values.splitlines()) + "\n"
